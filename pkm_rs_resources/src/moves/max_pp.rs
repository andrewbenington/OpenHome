use crate::{metadata_source::MetadataSource, moves::MoveSlot};

pub fn get_base_max_pp(move_index: u16, source: MetadataSource) -> Option<u8> {
    let Some(slice_by_index) = slice_for_source(source) else {
        return None;
    };

    slice_by_index.get(move_index as usize).copied()
}

fn get_raised_max_pp(move_index: u16, source: MetadataSource, pp_ups: u8) -> Option<u8> {
    let Some(base_max_pp) = get_base_max_pp(move_index, source) else {
        return None;
    };

    let with_pp_ups = match source {
        MetadataSource::RedBlue
        | MetadataSource::Yellow
        | MetadataSource::GoldSilver
        | MetadataSource::Crystal
            if base_max_pp == 40 =>
        {
            base_max_pp + (pp_ups * 7)
        }
        _ => base_max_pp + (pp_ups * (base_max_pp / 5)),
    };

    Some(with_pp_ups)
}

pub fn adjust_pp_between_games(
    from_source: MetadataSource,
    to_source: MetadataSource,
    move_slot: MoveSlot,
) -> Option<u8> {
    let index_inner = move_slot.move_index.0?;
    let index_u16 = index_inner.get();

    let from_source_max_pp = get_raised_max_pp(index_u16, from_source, move_slot.pp_ups)?;
    let to_source_max_pp = get_raised_max_pp(index_u16, to_source, move_slot.pp_ups)?;
    let missing_pp = from_source_max_pp.checked_sub(move_slot.pp)?;

    Some(to_source_max_pp - missing_pp)
}

